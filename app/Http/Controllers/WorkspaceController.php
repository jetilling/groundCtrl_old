     */
    public function store(Request $request)
    {
        $attributes = request()->validate([
            'name' => ['required', 'min:3'],
            'description' => 'required',
            'primary_icon_color' => 'required',
            'secondary_icon_color' => 'required'
        ]);

        Workspace::create($attributes);

        return redirect('/home/workspaces');
    }

